/*
Exercise 2.1 (Converting Celsius to Fahrenheit) 
Write a program that reads a Celsius degree in double from the console, 
then converts it to ahrenheit and displays the result. 
The formula for the conversion is as follows: fahrenheit = (9 / 5) * celsius + 32
 */

import java.util.Scanner;

public class Exercise02_01 {
	public static void main(String[] args) {
		// make scanner object
		Scanner input = new Scanner(System.in);
		System.out.print("Enter a degree in Celsius: ");

		// get input from user
		double deg = input.nextDouble();

		// calculate c to 
		double fahrenheit = (9.0 / 5) * deg + 32;

		// printout output
		System.out.println(deg + " Celsius is "+ fahrenheit+ " Fahrenheit");
	}
}
